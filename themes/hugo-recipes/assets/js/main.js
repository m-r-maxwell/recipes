// Highlight nutrition/macro lines (headings or paragraphs containing
// calories, macros, protein, carbs, or fat) by adding the `nutrition`
// class so CSS can style them consistently.
document.addEventListener('DOMContentLoaded', function () {
	try {
		const needle = /calori|macro|protein|carb|fat/i;
		// Avoid matching generic list items (li) so ingredient lists aren't
		// accidentally highlighted; only check headings and paragraphs.
		const candidates = Array.from(document.querySelectorAll('h1,h2,h3,h4,p'));
		candidates.forEach(el => {
			if (el.textContent && needle.test(el.textContent)) {
				el.classList.add('nutrition');
			}
		});
	} catch (e) {
		// be conservative — don't break the page if JS errors
		console.error('nutrition highlight error', e);
	}
});
